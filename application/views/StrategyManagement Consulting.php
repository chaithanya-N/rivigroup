          <h2 class="caseser">Summary</h2>
          <p style="text-align:left;">During this appointment, the newly elected mayor of the area wanted to ensure that all the government proceedings of the area are carried out efficiently. For this, RFPs (Request for proposal) were generated for analysis and BPR studies of the city’s ongoing critical processes.  These constituted HR, finance, procurement, customer service and IT services. RiVi played a major role in evaluating the key IT management processes and suggesting measures for improvement.</p>
		</div>
		<div class="col-md-4 text-center">
			<h2 class="caseser">Problem</h2>
			<p style="text-align:left;">With the changes in the administration and leadership of the city, new opportunities were laid basing on the efficiency and effectiveness of the services government provided to the city. These services were a combined effort of the past governance and were inherited down to the present administration. There was not much improvement though. However, the new mayor demanded an accurate statistics of the important proceedings in the city before investing a lump sum budget in the projects. There was scarcity of funds, resources and project related evaluations and functional departments and services were the areas of improvement.</p>
		</div>
		<div class="col-md-4 text-center">
			<h2 class="caseser">Approach</h2>
			<p style="text-align:left;">The integral stakeholders of the city were interviewed to extract data regarding IT management processes. It was these IT management processes that represented the governance mechanism enjoyed by the city before taking up technology decisions.<br>
           These stagnant processes were compared with the optimal practices. With a proper collection of data, analysis and review, the comparison and evaluation of the two processes were formulated and represented through a report.</p>
		</div>
	</div>
	</div> 
	<div class="container">
	<div class="col-md-12">
		<p style="font-weight:400;padding:15px;text-align:left; ">A 600+ page report was prepared and sent to the city’s Chamber of Commerce, who provided the funds to the city for the project. A final oral presentation, along with several interim presentations, were also provided to the Chamber and other leading stakeholders in order to win their vote of confidence and gain their support and buy-in.</p><br>
		<p style="font-weight: 400;">The report was accepted as the final mandate, to be used by the CIO, to expedite the implementation of the recommended changes and improvements.A time line was provided dividing the recommended changes into the following categories: Triage (immediate attention), 6-Month Plan, 1-Year Plan and 3-Year Plan horizons.</p><br>
		<p style="font-weight: 400;">The report and its recommendations were sanctioned by the incoming CIO in the 2004-2006 City IT Strategic Plan:</p><br>
		<p>“Several recommendations were made by the RiVi report that would help link information technology to the City’s business goals and directions, and would allow the City to use technology more effectively in providing high quality services to the public…”</p><br>
		<p>The City’s strategic plan document also endorsed the RiVi Study and its recommendations, putting forth the following major accomplishments achieved because of RiVi’s recommendations:</p><br>
